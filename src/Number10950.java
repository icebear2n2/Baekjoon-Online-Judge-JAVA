import java.util.Scanner;

public class Number10950 {
    public static void main(String[] args) {
        int n;

        Scanner sc = new Scanner(System.in);

        n = sc.nextInt();

        for (int i=0; i<n; i++) {
            int a, b;

            a = sc.nextInt();
            b = sc.nextInt();

            System.out.println(a + b);

        }
    }
}
